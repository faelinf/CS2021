public class CalculoMedia {
    public static void main(String args[]) {

        int primeiramedia = (9 + 8 + 7) / 3;
        System.out.println(primeiramedia);

        int segundamedia = (6 + 5 + 4) / 3;
        System.out.println(segundamedia);

        int soma = segundamedia + primeiramedia;
        System.out.println(soma);

        int mediaDaMedia = soma / 2;
        System.out.println(mediaDaMedia);

    }
}
